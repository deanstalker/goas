package types

const (
	GoTypeIgnored = "ignored"
)

var basicGoTypes = map[string]bool{
	"bool":       true,
	"uint":       true,
	"uint8":      true,
	"uint16":     true,
	"uint32":     true,
	"uint64":     true,
	"int":        true,
	"int8":       true,
	"int16":      true,
	"int32":      true,
	"int64":      true,
	"float32":    true,
	"float64":    true,
	"string":     true,
	"complex64":  true,
	"complex128": true,
	"byte":       true,
	"rune":       true,
	"uintptr":    true,
	"error":      true,
}

// IsBasicGoType checks if a typename is a basic go type
func IsBasicGoType(typeName string) bool {
	_, ok := basicGoTypes[typeName]
	return ok
}
